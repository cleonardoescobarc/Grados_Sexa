print("Convercion de grados a radianes")
print("Asumamos que pi = 3.1416")
grados = float(input("Ingrese el valor de grados: "))

gradosR = grados * (2*3.1416/180)

print("La cantidad de radianes es: ", gradosR)

gradosS = gradosR * 180 / (3.1416 * 2)

print("La cantidad de grados sexa es: ", gradosS)
